                <div>
                    <h3>
                       

                        <p>
                            Įsigytas prekes galite nemokamai atsiimti parduotuvėje "Rubinas" Telšiuose arba pasirinkti, kad prekė būtų pristatyta Jūsų nurodytu adresu. 
                        </p>

                        <p><strong>Nemokamas atsiėmimas parduotuvėse</strong></p>

                        <p>
                            Elektroninėje parduotuvėje užsakytas prekes galite nemokamai atsiimti parduotuvėje "Rubinas" Telšiuose. Norėdami patys atsiimti prekes nemokamai, užsisakydami jas pasirinkite „Atsiimti parduotuvėje“.
                        </p>
                        
                        <p><strong>Atsiimant užsakymą turite:</strong></p>
                        
                            <ul>
                                <li>pasakyti užsakymo numerį;</li>
                                <li>pateikti asmens tapatybės dokumentą (asmens pasą, asmens tapatybės kortelę ar vairuotojo pažymėjimą).</li>
                            </ul>
                        
                        <p><strong>Kada atsiimti prekes?</strong></p>

                        <p>
                            Paruošę Jūsų užsakymą, elektroniniu paštu informuosime, kad jau galite atvykti jo atsiimti. Po šio pranešimo per 3 darbo dienas lauksime Jūsų atvykstant.
                        </p>
                        <p>
                            Jei manote, kad nespėsite atsiimti užsakymo per nurodytą laiką, būtinai praneškite apie tai elektroniniu paštu pagalba@senukai.lt, kitaip Jūsų užsakymas bus atšauktas. 
                        </p>

                        <p><strong>Pristatymas į namus</strong></p>

                        <p>
                            Užsisakydami prekę, pasirinkite, kad ji būtų pristatyta Jūsų nurodytu adresu. Už ją galite atsiskaityti mokėdami banko kortele, „Mokipay“ pinigine, elektronine bankininkyste arba sumokėti grynaisiais prekę pristačiusiam kurjeriui.
                        </p>
                        <p>
                            Įprastai prekės pristatomos per 1–5 darbo dienas nuo užsakymo pateikimo.
                        </p>

                        <p><strong>Pristatymo kaina</strong></p>

                        Prekes pristatome nurodytu adresu Nemokamai.

